    private void btnCAvalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCAvalActionPerformed

        Avaliacao avalSelecionado;
        int linha = tblConsultaAval.getSelectedRow();
        if (linha >= 0) {
            avalSelecionado = (Avaliacao) tblConsultaAval.getValueAt(linha, 0);
            ctrlIHC.setAvalSelecionado(avalSelecionado);
            setVisible(false);

            if (janProx == "compra") {
                ctrlIHC.janelaComprar();
            } else if (janProx == "cadastrarCli") {
                ctrlIHC.janelaCadastrarCliente();
            } else if (janProx == "vender") {
                ctrlIHC.janelaVender();
            } else if (janProx == "novaAval") {
                ctrlIHC.janelaNovaAvalicao();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente");
        }

    }//GEN-LAST:event_btnCAvalActionPerformed

