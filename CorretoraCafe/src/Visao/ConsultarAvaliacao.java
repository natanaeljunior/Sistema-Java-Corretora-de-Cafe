    private void btnEAvalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEAvalActionPerformed
        int confirma = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir AVALIAÇÃO? ");
        if (confirma == 0) {

            int linha = tblConsultaAval.getSelectedRow();

            if (linha >= 0) {
                try {
                    Object obj = (Object) tblConsultaAval.getValueAt(linha, 0);
                    ctrlIHC.getCtrlDominio().excluiraAval(obj);
                    ((DefaultTableModel) tblConsultaAval.getModel()).removeRow(linha);
                    JOptionPane.showMessageDialog(this, "Avaliação Excluida com Sucesso!");
                    ctrlIHC.atualizarAval();
                } catch (HibernateException ex) {
                    JOptionPane.showMessageDialog(this, "Voce não pode Apagar essa Avaliação");
                    Logger.getLogger(ConsultarAvaliacao.class.getName()).log(Level.SEVERE, null, ex);

                } catch (SQLException ex) {
                    Logger.getLogger(ConsultarAvaliacao.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnEAvalActionPerformed

